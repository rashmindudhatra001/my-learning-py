
# This program converts temperature from Celsius to Fahrenheit.

# Formula:
# Fahrenheit = (Celsius * 9/5) + 32

# Taking user input
celsius = float(input("Enter temperature in Celsius: "))

# Conversion formula
fahrenheit = (celsius * 9/5) + 32

# Display the result
print(f"{celsius}°C is equal to {fahrenheit}°F")
